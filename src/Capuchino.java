public class Capuchino {
    private int water;
    private int milk;
    private int beans;

    public Capuchino(int water, int milk, int beans) {
        this.water = water;
        this.milk = milk;
        this.beans = beans;
    }
    public int getWater() {
        return water;
    }

    public int getMilk() {
        return milk;
    }

    public int getBeans() {
        return beans;
    }
}
